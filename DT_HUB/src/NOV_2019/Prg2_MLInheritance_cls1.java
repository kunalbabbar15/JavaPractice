package NOV_2019;

public class Prg2_MLInheritance_cls1 
{

	public int MethodAdd(int a, int b)
	{
		int c = a+b;
		System.out.println("I am Parent method a+b "+c);
		return c;
		
	}

	public int MethodSub(int a, int b)
	{
		int c = a-b;
		System.out.println("I am Parent method a-b "+c);
		return c;
		
	}
}
